public class ej9tema2 {
    
     public static void main(String[] args) {
     // Paso 1: Declarar la constante PI
        final double PI = 3.14159;
       
        // Paso 2: Declarar la variable para el radio
        double radio = 3.55;
       
        // Paso 3: Calcular la longitud de la circunferencia
        // La fórmula es: longitud = 2 * PI * radio
        double longitud = 2 * PI * radio;
       
        // Paso 4: Mostrar el resultado por pantalla
        System.out.println("La longitud de una circunferencia cuyo radio vale " + radio + " sería igual a: " + longitud + " metros.");
    }
}

