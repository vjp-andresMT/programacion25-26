public class ej14tema2 {
    public static void main(String[] args) {
     // Paso 1: Declarar la constante PI
        final double PI = 3.14159;
       
        // Paso 2: Declarar la variable para el radio
        double radio = 5.2;
       
        // Paso 3: Calcular el área de la circunferencia
        // La fórmula es: área = PI * radio * radio
        double area = PI * radio * radio;
       
        // Paso 4: Mostrar el resultado por pantalla
        System.out.println("El área de una circunferencia cuyo radio vale " + radio + " sería igual a: " + area + " centímetros cuadrados.");
    }
}
